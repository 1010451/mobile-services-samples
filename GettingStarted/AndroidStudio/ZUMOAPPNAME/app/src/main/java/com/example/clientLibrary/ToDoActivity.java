    /**
     * Concatenate queries
     *
     */
    public void concat(View view) {
        new AsyncTask<Void, Void, Void>() {
            @Override
            protected Void doInBackground(Void... params) {
                try {
                    final MobileServiceList<ToDoItem> result = mToDoTable
                            .where().year("__createdAt").eq(2014)
                            .and().startsWith("text", "h")
                            .or().field("complete").eq(true)
                            .select("id", "complete", "text")
                            .orderBy("id", QueryOrder.Ascending).top(8)
                            .execute().get();
                    runOnUiThread(new Runnable() {

                        @Override
                        public void run() {
                            mAdapter.clear();
                            for (ToDoItem item : result) {
                                mAdapter.add(item);
                            }
                        }
                    });
                } catch (Exception exception) {
                    createAndShowDialog(exception, "Error");
                }
                return null;
            }
        }.execute();
    }


